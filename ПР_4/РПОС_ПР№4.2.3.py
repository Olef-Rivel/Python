def is_lucky(num):
    """Является ли 'num' номером счастливого билета?

    Параметры:
        num (int): номер билета.

    Результат:
        bool: True - если количество четных цифр равно количеству нечетных, иначе False.
    """
    digits = [int(d) for d in str(num)]  # Разбиваем число на цифры
    even_count = sum(1 for d in digits if d % 2 == 0)  # Подсчитываем четные цифры
    odd_count = len(digits) - even_count  # Подсчитываем нечетные цифры
    return even_count == odd_count  # Проверяем условие счастливого билета

def lucky_numbers(a, b):
    """Вернуть список счастливых номеров от 'a' до 'b'.

    Параметры:
        a (int): начало диапазона.
        b (int): конец диапазона.

    Результат:
        list: список счастливых номеров.
    """
    return [num for num in range(a, b + 1) if is_lucky(num)]

# Ввод границ диапазона
a = int(input("Введите начало диапазона (a): "))
b = int(input("Введите конец диапазона (b): "))

# Вывод должен быть осуществлен в строку с одним пробелом-разделителем
print(f"Первый номер билета: {a}")
print(f"Последний номер билета: {b}")
print(" ".join(map(str, lucky_numbers(a, b))))
